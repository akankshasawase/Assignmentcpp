//write a program to swap two numbers.
# include<iostream>
using namespace std;
int main()
{
    int a=25,b=39,c;
    cout<<"input 1st number:"<<a<<endl;
    cout<<"input 2nd number:"<<b<<endl;
    c=a;
    a=b;
    b=c;
    cout<<"swaping the 1st number:"<<a<<endl;
    cout<<"swaping the 2nd number:"<<b<<endl;
    return 0;


}
